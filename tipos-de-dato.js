// Valores solicitados
console.log("42 ->", typeof 42); // number
console.log("'Veinticinco' ->", typeof 'Veinticinco'); // string
console.log("-666 ->", typeof -666); // number
console.log("true ->", typeof true); // boolean
console.log("0 ->", typeof 0); // number
console.log("' ' ->", typeof " "); // string
console.log("null ->", typeof null); // object
console.log("undefined ->", typeof undefined); // undefined
console.log("FALSE ->", typeof FALSE); // error (no está definido)

